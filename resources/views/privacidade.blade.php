<p class=MsoNormal><b>Política de Privacidade</b></p>

<p class=MsoNormal><b>Política Geral de Segurança e Privacidade</b></p>

<p class=MsoNormal>&nbsp;</p>

<p class=MsoNormal>A mera navegação na Internet implica a recolha e transmissão
de dados de modo a ser possível efetivar a comunicação entre os equipamentos
eletrónicos. Existem ainda outros dados que podem ser recolhidos e transmitidos
para uma experiência de uso mais confortável e interessante. Alguns desses
dados podem ser considerados dados pessoais.</p>

<p class=MsoNormal>&nbsp;</p>

<p class=MsoNormal>Para maior transparência e considerando a diversidade de
dados tratados e de finalidades, de seguida proceder-se-á à descrição e contextualização
de toda a informação pessoal, ou potencialmente pessoal, que é tratada pela
navegação no presente website.</p>

<p class=MsoNormal>&nbsp;</p>

<p class=MsoNormal>A proteção da privacidade e dos dados pessoais é uma
prioridade para a Empresa RedFénix Engenharia LDA (adiante abreviadamente
designada por RedFénix) e um claro compromisso assumido por nós.</p>

<p class=MsoNormal>&nbsp;</p>

<p class=MsoNormal>Esta política diz respeito às práticas de privacidade no
âmbito da atividade realizada pela REDFÉNIX incluindo os portais, sítios online
e outros serviços geridos por esta. Os dados pessoais que nos forneça durante a
interação com os vários serviços serão tratados com as garantias de segurança e
confidencialidade exigidas pelo enquadramento legal relativo à proteção de
dados pessoais.</p>

<p class=MsoNormal>&nbsp;</p>

<p class=MsoNormal>Os portais e sítios sob a responsabilidade da REDFÉNIX, no
âmbito dos serviços disponibilizados, contêm links para outros sítios sob a
responsabilidade de entidades terceiras. A política de privacidade da REDFÉNIX
aplica-se exclusivamente aos sítios e serviços sob sua responsabilidade. Quando
acede a sítios de outras entidades, ainda que a partir dos sítios sob
responsabilidade da REDFÉNIX, deverá ler as respetivas políticas de
privacidade.</p>

<p class=MsoNormal>&nbsp;</p>

<p class=MsoNormal>O acesso e a utilização dos serviços, portais e sítios, sob
a responsabilidade da REDFÉNIX, devem ser livres por parte do utilizador
presumindo a REDFÉNIX que este leu, compreendeu e aceitou os Termos e Condições
da sua utilização.</p>

<p class=MsoNormal>&nbsp;</p>

<p class=MsoNormal>Quaisquer tentativas de alterar a informação, ou qualquer
outra ação que possa causar dano e pôr em risco a integridade do sistema, são
estritamente proibidas de acordo com a legislação em vigor. O utilizador
obriga-se a cumprir escrupulosamente a legislação aplicável, nomeadamente, em
matéria de criminalidade informática e de direitos de propriedade intelectual,
sendo exclusivamente responsável pela infração destes normativos.</p>

<p class=MsoNormal>&nbsp;</p>

<p class=MsoNormal>&nbsp;</p>

<p class=MsoNormal><b>Encarregado de Proteção de Dados</b></p>

<p class=MsoNormal>&nbsp;</p>

<p class=MsoNormal>A REDFÉNIX designou um Encarregado de Proteção de Dados que
poderá ser contactado através do endereço eletrónico geral@redfenix.pt, ou por
carta registada com aviso de receção para Alameda 1 de março nº 35, 2300-431 Tomar).</p>

<p class=MsoNormal>&nbsp;</p>

<p class=MsoNormal><b>Privacidade</b></p>

<p class=MsoNormal>&nbsp;</p>

<p class=MsoNormal>A REDFÉNIX respeita o seu direito à privacidade. Os dados
solicitados destinam-se à prestação do serviço por si solicitado, não sendo
recolhidos dados pessoais que não sejam necessários à prestação do serviço ou
sem o seu consentimento.</p>

<p class=MsoNormal>&nbsp;</p>

<p class=MsoNormal>A interação com os serviços, portais e sítios da REDFÉNIX
não exige informação do utilizador, salvo quando, para propósitos expressamente
identificados, o registo seja declarado necessário e seja por aquele autorizado
de acordo com os termos seguintes.</p>

<p class=MsoNormal>&nbsp;</p>

<p class=MsoNormal>&nbsp;</p>

<p class=MsoNormal><b>Responsável pelo Tratamento</b></p>

<p class=MsoNormal>&nbsp;</p>

<p class=MsoNormal>A REDFÉNIX enquanto responsável pelo tratamento de dados:</p>

<p class=MsoNormal>Assegura que o tratamento dos seus dados pessoais é efetuado
no âmbito das finalidades para as quais os mesmos foram recolhidos ou para
finalidades compatíveis com aquelas;</p>

<p class=MsoNormal>Recolhe, utiliza e conserva apenas os dados pessoais
necessários para a finalidade em causa;</p>

<p class=MsoNormal>Não procede a qualquer transmissão de dados pessoais para
fins comerciais ou de publicidade;</p>

<p class=MsoNormal>Trata os dados pessoais para fins legalmente previstos ou
para a prossecução de serviços a seu pedido.</p>

<p class=MsoNormal><b>Tipologia de Dados Pessoais que recolhemos</b></p>

<p class=MsoNormal>&nbsp;</p>

<p class=MsoNormal>Os dados pessoais sujeitos a tratamento por parte da REDFÉNIX
são de natureza diversa atenta a prestação de serviços promovidos nas
plataformas eletrónicas e restantes serviços sob a sua gestão.</p>

<p class=MsoNormal>&nbsp;</p>

<p class=MsoNormal>De acordo com o tipo de serviços prestados podem ser
recolhidos: o endereço IP; o nome; a idade; o número de identificação fiscal; o
número de identificação civil; o número de segurança social; o número de
documento de identificação; a morada; o endereço eletrónico; e o número de
telemóvel.</p>

<p class=MsoNormal>&nbsp;</p>

<p class=MsoNormal>&nbsp;</p>

<p class=MsoNormal><b>Segurança de Dados Pessoais</b></p>

<p class=MsoNormal>&nbsp;</p>

<p class=MsoNormal>A REDFÉNIX, na prossecução das suas atividades, utiliza um
conjunto de tecnologias e procedimentos de segurança adequados à proteção dos
seus dados pessoais, protegendo o acesso ou divulgação não autorizados,
nomeadamente:</p>

<p class=MsoNormal>&nbsp;</p>

<p class=MsoNormal><b>Acesso e controlo dos seus Dados Pessoais</b></p>

<p class=MsoNormal>&nbsp;</p>

<p class=MsoNormal>Possibilitamos, a seu pedido, o acesso, a retificação e o
apagamento dos dados pessoais. Tem também o direito de se opor ao processamento
dos seus dados pessoais.</p>

<p class=MsoNormal>&nbsp;</p>

<p class=MsoNormal>Caso a utilização dos seus dados pessoais seja baseada no
consentimento, tem o direito de o retirar, sem comprometer a validade do
tratamento de dados efetuado até esse momento. Através do encarregado de
proteção de dados (REDFÉNIX) pode solicitar:</p>

<p class=MsoNormal>&nbsp;</p>

<p class=MsoNormal>Informação acerca dos seus dados;</p>

<p class=MsoNormal>Informação acerca do processamento;</p>

<p class=MsoNormal>A finalidade subjacente ao tratamento;</p>

<p class=MsoNormal>A correção, ou atualização, de dados;</p>

<p class=MsoNormal>Os seus dados num formato aberto;</p>

<p class=MsoNormal>O apagamento dos dados, sem prejuízo da legislação aplicável
ao tratamento em causa.</p>

<p class=MsoNormal>Arquivo de Dados Pessoais</p>

<p class=MsoNormal>&nbsp;</p>

<p class=MsoNormal>Os seus dados pessoais são conservados pela REDFÉNIX pelo
período necessário ao tratamento, de acordo com a finalidade respetiva.</p>

<p class=MsoNormal>&nbsp;</p>

<p class=MsoNormal>Os períodos de conservação podem alterar-se quando o
interesse público associado, motivos históricos, científicos ou estatísticos o
justifiquem, comprometendo-se a REDFÉNIX a adotar as adequadas medidas de
conservação e segurança.</p>

